// есть волшебная переменная в JavaScript "__dirname" - это глобальная переменная созданная в самом JavaScript 
// и она указывается на то, где сейчас находится файл из которого мы пытаемся достать что-либо.
// Например, у нас есть файл index.js и он лежит в папке Lesson7, и чтобы компьютер понял, что этот файл (путь к файлу) лежит в Lesson7 
// мы должны обратиться как то к нему и для этого придумали вот эту глобальную переменную __dirname.
// __dirname показывает где лежит этот скрипт, который мы написали.
// Для того чтобы нам найти нужный нам файл, который мы импортируем мы будем использовать модуль Path и глобальную переменную __dirname


