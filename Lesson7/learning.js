const fullPath = path.join(__dirname, "index.html");  // он составил директорию файла index.html
// join() - метод, он суммирует все аргументы и выдает наш общий путь для правильной понимании системой,
// где был запущен код.  Грубо говоря это сборщик пути, где запущен ваш код на какой-то системе для Windows составит отдельный путь,
// для Linux отдельный, для MacOS отдельный.
// Тут мы index.html импортируем правильно для каждой системы.

