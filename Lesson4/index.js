// Стрелочные функции (arrow function)
// Есть разница между обычным function и стрелочной функции

// Формат записи:
// const sum - это название нашей функции
// через равно указываем аргументы нашей функции, которые мы будем принимать в нее
// и с помощью "=>" - мы делаем стрелку, то есть стрелочная функция
// Создаем константу, в которую кладем функцию
const SumArrowFunction = (a, b) => {
    return a + b;
}

summa2 = SumArrowFunction(12, 32);
console.log(summa2);

